            <h3 class="text-center my-2">Hatha yoga</h3>
              <br>
                <p>
                  There are several styles and styles of yoga, but the most basic is "Hatayoga". Take a basic pose while repeating the deep breaths of inhaling and exhaling well. Hatha yoga is so old in origin that there are various theories in history, but it is said that it started around the 10th to 13th century AD. <br>
                  The original purpose of yoga was to deepen meditation while taking deep breaths. For this reason, yoga postures have been used to maintain the mind and body, and to make it easier to meditate. <br>
                  In Sanskrit, “ha” means sun = breath, and “tha” means moon = breath. The sun and the moon symbolize the opposing things of the world, such as the sun and the shade, the top and the bottom, the man and the woman, the mind and the body. On the other hand, the word "yoga" has the meaning of "connect." Hatha yoga has the meaning of connecting and harmonizing conflicting things. <br>
                  Cardo's Hatha Yoga lessons focus on the basic poses of all yoga. Even if you are doing yoga for the first time or if you are worried about the hardness of your body, it is a content that you can join without difficulty. It's a great program for learning the basics of yoga. Please practice while paying attention to the breathing (balance between inhaling and exhaling), which is the characteristic of Hatha Yoga.
