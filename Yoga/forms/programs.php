            It has been reported that overseas celebrities have adopted it as a beauty method, and in the 90's, a yoga boom suddenly occurred in Europe and the United States. <br>
            Power yoga is categorized as active yoga because it incorporates many dynamic poses and movements.
            And breathing in conjunction operation, as "Ashtanga Yoga" to deploy multiple pose to flow, put the emphasis on the body axis and pelvis correction " Iyengar yoga combination of" flows through the pose as "dynamic" and "static" To do so.
            It is said to be a “modern version of yoga” that has evolved according to the body shape and lifestyle of modern people, and compared to classical yoga poses, the degree of freedom in arrangement is relatively high. In addition, it is characterized by a large amount of exercise because it keeps holding poses repeatedly. The aim is to improve physical strength and concentration by incorporating a balance of "quiet" poses and meditation into hard yoga exercises. <br><br>
